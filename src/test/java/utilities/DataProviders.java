package utilities;

import java.io.IOException;

import org.testng.annotations.DataProvider;

public class DataProviders {

    // DataProvider 1
    @DataProvider(name = "LoginData")
    public String[][] getData() throws IOException {

        String path = ".\\testData\\Opencart_LoginData.xlsx";   // taking x1 file from testData

        ExcelUtility xlutil = new ExcelUtility(path);           // creating an object for XLUtility

        int totalrows = xlutil.getRowCount("Sheet1");
        int totalcols = xlutil.getCellCount("Sheet1", 1);

        String logindata[][] = new String[totalrows][totalcols]; // 2D array for storing excel data

        for (int i = 1; i <= totalrows; i++) {    // read rows from excel
            for (int j = 0; j < totalcols; j++) { // read columns
                logindata[i - 1][j] = xlutil.getCellData("Sheet1", i, j);  // store data into array
            }
        }

        return logindata;  // returning two dimensional array
    }
}
